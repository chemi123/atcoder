void dfs(const vector<vector<int>>& graph, vector<vector<mint>>& dp, int current, int parent) {
  for (auto next : graph[current]) if (next != parent) {
    dfs(graph, dp, next, current);
    dp[current][0] *= (dp[next][0] + dp[next][1]);
    dp[current][1] *= dp[next][0];
  }
}

